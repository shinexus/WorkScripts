SELECT
  YWORGNAME,
  BILLNO,
  YWBILLNO,
  REMARK,  
  CASE JTAXRATE
    WHEN 17
    THEN SUM(JTaxTotal)
  END AS TaT17,
  CASE JTAXRATE
    WHEN 13
    THEN SUM(JTaxTotal)
  END AS TaT13,
  SUM(WJTOTAL) AS WJTOTAL,
  SUM(HJTOTAL) AS HJTOTAL
FROM
  TACPJSJXBODY
WHERE
  (
    BILLNO = '1001YFJS201205100091'
  )
GROUP BY
  YWORGNAME,
  BILLNO,
  YWBILLNO,
  REMARK,
  JTAXRATE  
ORDER BY
  YWBILLNO
